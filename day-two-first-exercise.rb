# Methods, return values, and conditions
# Parameters are when you define the method
# Arguments are when you supply a value when invoking the method

# A parameter is a variable in a method definition. When a method is
# called, the arguments are the data you pass into the method's parameters.
# Parameter is variable in the declaration of function. Argument is the
# actual value of this variable that gets passed to function.


greeting = "Hello!"

def greet(g)
  puts g
end

greet(greeting)
greet("What's up!")
greet("Good morning!")